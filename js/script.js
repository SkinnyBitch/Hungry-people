function styleSelect(e) {
    let foundStyle = document.querySelector('.select');
    if (foundStyle != e.target) {
        e.target.classList.add('select');
        foundStyle.classList.remove('select');
    }
    else {
        e.target.classList.add('select');
    }
}
function showMenu(menu_arg) {
    let menuForShow="";
    for (let item of menu_arg) {
        menuForShow += `<div class="component_menu"> <div class="component_menu__product"> <h3>${item.name}</h3> <p>${item.description}</p></div> <div class="component_menu__price"> <h3>${item.price}</h3></div></div>`;
    }
    outMenu.innerHTML = menuForShow;
}
